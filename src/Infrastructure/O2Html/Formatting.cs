namespace O2Html;

/// <summary>
/// Specifies formatting options for the <see cref="HtmlSerializer"/>.
/// </summary>
public enum Formatting
{
    /// <summary>
    /// No special formatting is applied. This is the default.
    /// </summary>
    None = 0,

    /// <summary>
    /// Causes child elements to be on new lines.
    /// </summary>
    NewLines = 1
}
